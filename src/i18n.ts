const locale = ref<Locale>(resolveSystemLocale());
let preference: Language = "system";

function resolveSystemLocale(): Locale {
  return navigator.language.toLowerCase().startsWith("zh") ? "zh-CN" : "en";
}

export function setLanguage(language: Language) {
  preference = language;
  locale.value = language === "system" ? resolveSystemLocale() : language;
  document.documentElement.lang = locale.value;
}

export function refreshSystemLanguage() {
  if (preference === "system") setLanguage("system");
}

export function currentLocale(): Locale {
  return locale.value;
}

export function t(key: string, variables: Variables = {}): string {
  const template = locale.value === "en" ? english[key] ?? key : key;
  return template.replace(/\{(\w+)\}/g, (_, name: string) => String(variables[name] ?? `{${name}}`));
}
